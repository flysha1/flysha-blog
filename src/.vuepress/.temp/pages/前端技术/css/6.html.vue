<p><strong>单行文本两端对齐</strong></p>
<ol>
<li>首先，所有主流浏览器都支持 <code v-pre>text-align</code> 的 <code v-pre>justify</code> 属性值</li>
<li>其次，在这个例子中每个 <code v-pre>p</code> 都只有一行（所以既是第一行也是最后一行），所以无法通过定义 <code v-pre>text-align: justify</code> 来实现两端对齐，因为 <code v-pre>text-align: justify</code> 不会处理块级内容文本的最后一行</li>
<li>再次，好在有一个专门用来处理最后一行对齐的属性 <code v-pre>text-align-last</code>，可以通过定义 <code v-pre>text-align-last: justify</code> 来实现单行文本两端对齐</li>
<li>综上所述，我们无法简单的通过使用 <code v-pre>text-align</code> 或者 <code v-pre>text-align-last</code> 来实现单行文本两端对齐。那么可以换个思路，想办法让它们变成多行文本，使用伪元素派生一行新的占位内容是个不错的选择，此时再实现两端对齐，只需要 <code v-pre>text-align</code> 就行了</li>
</ol>
